package spacecraft;

public class Chandrayaan3 {
    private int x, y, z;
    private Direction direction;

    public Chandrayaan3(int x, int y, int z, Direction direction) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.direction = direction;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }

    public Direction getDirection() {
        return direction;
    }

    
}
